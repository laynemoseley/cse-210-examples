class Animal {}

class Dog: Animal {
    public void Bark() {
        Console.WriteLine("bark bark bark");
    }
}

class Cat: Animal {
    public void Meow() {
        Console.WriteLine("meooooooow");
    }
}

class Chicken: Animal {
    public void Cluck() {
        Console.WriteLine("cluck");
    }
}

class Pig: Animal {
    public void Oink() {
        Console.WriteLine("oiiiiiiiiiiiink");
    }
}

class Horse: Animal {
    public void Neigh() {
        Console.WriteLine("nah");
    }
}

class Panda: Animal {
    public void Bleat() {
        Console.WriteLine("Whoa, the Furious Five! You're so much bigger than your action figures! Except for you, Mantis. You're about the same.");
    }
}
